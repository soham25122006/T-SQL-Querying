CREATE TABLE PUBLISHER(
    PUBLISHERID INT PRIMARY KEY IDENTITY(1,1),
    PUBLISHERNAME VARCHAR(100) NOT NULL UNIQUE,
    CITY VARCHAR(50) NOT NULL
);

